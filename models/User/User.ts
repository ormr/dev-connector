import mongoose, { Model, Schema } from 'mongoose';

const UserSchema: Schema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  avatar: {
    type: String
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const User: Model<mongoose.Document> = mongoose.model('user', UserSchema);

export {
  User
};